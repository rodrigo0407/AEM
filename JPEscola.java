    private void jButtonSalvarESActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarESActionPerformed
        // TODO add your handling code here:
        
        //Verificar campo obrigatorio
        
         if(jTextFieldNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo Nome");
            jTextFieldNome.requestFocus();
        }
        else if(jTextFieldCnpj.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo CNPJ");
            jTextFieldNome.requestFocus();
        }else{
            
            //criar o objeto
            Escola escola = new Escola();
            
            //preencha os dados no objeto
            escola.setNome(jTextFieldNome.getText());
            escola.setCnpj(jTextFieldCnpj.getText());
            escola.setEndereco(jTextFieldEndereco.getText());
            escola.setAlunoid(jTextFieldAlunoid.getText());
            
            //Se o codigo for Vazio de um INSERT
            if(jTextFieldInep.getText().equals("")){
                //inserir no banco de dados
                if(repescola.inserir(escola)==true){
                JOptionPane.showMessageDialog(null, "Executado com sucesso");
                limparCampos();
                preencherTable();
            }else{
                    JOptionPane.showMessageDialog(null,"Falha ao executar");
            }        
            }else{
                //se o código existir então é um update
            escola.setInep(Integer.parseInt(jTextFieldInep.getText()));
            //chamamos o método para alterar os dados
            if(repescola.atualizar(escola)==true){
                JOptionPane.showMessageDialog(null,"Executado com Sucesso");
                limparCampos();
                preencherTable();
                 }else{
                JOptionPane.showMessageDialog(null,"Falha ao Executar");
            }
            
            }
            
        }
    }//GEN-LAST:event_jButtonSalvarESActionPerformed

