public void preencherTable(){
    DefaultTableModel modelo = (DefaultTableModel)jTableListaES.getModel();
    modelo.setNumRows(0);
    
    for(Escola c : repescola.retornar()){
        modelo.addRow(new Object[]{
           //aqui vao ficar as colunas
            c.getInep(),
            c.getNome(),
            c.getCnpj(),
            c.getEndereco(),
            c.getAlunoid()
                
        });
    }
    
}
